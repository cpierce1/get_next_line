#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*res;
	int		i;
	int		sz;

	sz = 0;
	while (s1[sz])
		sz++;
	if (!(res = malloc(sizeof(char) * (sz + 1))))
		return (NULL);
	i = 0;
	while (s1[i])
	{
		res[i] = s1[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
